const buttonZodiac1 = document.getElementById("btnZodiac1");
const audiosZodiac1 = document.getElementById("audiosZodiac1");

audiosZodiac1.style.display ="none";

buttonZodiac1.addEventListener("click", (event) => {
    if(audiosZodiac1.style.display == "none"){
        audiosZodiac1.style.display ="block";
    } else {
        audiosZodiac1.style.display ="none";
    }
});

